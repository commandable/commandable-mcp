#!/usr/bin/env node

import crypto from 'node:crypto'
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const packageRoot = resolve(here, '..')
const migrationsRoot = resolve(packageRoot, 'src', 'db', 'migrations')
const outputPath = resolve(packageRoot, 'src', 'db', 'generated-migrations.ts')

function loadDialectMigrations(dialect) {
  const folder = resolve(migrationsRoot, dialect)
  const journalPath = resolve(folder, 'meta', '_journal.json')
  if (!existsSync(journalPath))
    throw new Error(`Missing migration journal: ${journalPath}`)

  const journal = JSON.parse(readFileSync(journalPath, 'utf8'))
  return journal.entries.map((entry) => {
    const sqlPath = resolve(folder, `${entry.tag}.sql`)
    if (!existsSync(sqlPath))
      throw new Error(`Missing migration SQL: ${sqlPath}`)

    const query = readFileSync(sqlPath, 'utf8')
    return {
      sql: query.split('--> statement-breakpoint').map(part => part),
      folderMillis: entry.when,
      hash: crypto.createHash('sha256').update(query).digest('hex'),
      bps: entry.breakpoints,
    }
  })
}

const sqliteMigrations = loadDialectMigrations('sqlite')
const pgMigrations = loadDialectMigrations('pg')

const output = `// This file is auto-generated by packages/core/scripts/generate-embedded-migrations.mjs.
// Do not edit it manually; regenerate it from src/db/migrations.

import type { MigrationMeta } from 'drizzle-orm/migrator'

export const sqliteMigrations: MigrationMeta[] = ${JSON.stringify(sqliteMigrations, null, 2)}

export const pgMigrations: MigrationMeta[] = ${JSON.stringify(pgMigrations, null, 2)}
`

writeFileSync(outputPath, output)
console.error(`[generate-embedded-migrations] Wrote ${outputPath}`)
