#!/usr/bin/env node

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const packageRoot = resolve(here, '..')
const mcpSrcDir = resolve(packageRoot, 'src', 'mcp')
const outputPath = resolve(mcpSrcDir, 'generated-assets.ts')

const assets = [
  { name: 'COMMANDABLE_README_CREATE', file: 'commandable_readme_create.md' },
  { name: 'COMMANDABLE_README_DYNAMIC', file: 'commandable_readme_dynamic.md' },
  { name: 'COMMANDABLE_README_STATIC', file: 'commandable_readme_static.md' },
  { name: 'BUILDER_GUIDE', file: 'builder_guide.md' },
]

const lines = [
  '// This file is auto-generated by packages/core/scripts/generate-embedded-assets.mjs.',
  '// Do not edit it manually; regenerate it from src/mcp/*.md.',
  '',
]

for (const { name, file } of assets) {
  const content = readFileSync(resolve(mcpSrcDir, file), 'utf8')
  // Use JSON.stringify to safely escape the string for embedding
  lines.push(`export const ${name}: string = ${JSON.stringify(content)}`)
  lines.push('')
}

writeFileSync(outputPath, lines.join('\n'))
console.error(`[generate-embedded-assets] Wrote ${outputPath}`)
