#!/usr/bin/env node

import crypto from 'node:crypto'
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const packageRoot = resolve(here, '..')
const srcPath = resolve(packageRoot, 'src', 'file-extractor', 'extract_file.py')
const outputPath = resolve(packageRoot, 'src', 'integrations', 'generated-extractor.ts')

const content = readFileSync(srcPath, 'utf8')
const hash = crypto.createHash('sha256').update(content).digest('hex')

const output = `// This file is auto-generated by packages/core/scripts/generate-embedded-extractor.mjs.
// Do not edit it manually; regenerate it from src/file-extractor/extract_file.py.

export const EXTRACT_FILE_PY: string = ${JSON.stringify(content)}

export const EXTRACT_FILE_PY_HASH: string = ${JSON.stringify(hash)}
`

writeFileSync(outputPath, output)
console.error(`[generate-embedded-extractor] Wrote ${outputPath}`)
